import math
print("Введите число a и b")
a = int(input("а: "))
b = int(input("b: "))
print("Сумма a и b: ", a+b)
print("Разница между a и b: ", abs(a-b))
print("Произведение a и b: ", a*b)
print("Частное от деления a на b: ", a/b)
print("Остаток от делени a на b: ", a%b)
print("Десятичный логарифм числа а: ", math.log(a,10) )
print("Результут возведения числа а в степень b: ",a**b )